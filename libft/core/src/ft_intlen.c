int					ft_intlen(int n)
{
	int				len;
	unsigned int	un;

	len = 1;
	un = 0;
	if (n < 0)
	{
		len++;
		un = un - n;
	}
	else
		un = n;
	while (un > 9)
	{
		un = un / 10;
		len++;
	}
	return (len);
}
